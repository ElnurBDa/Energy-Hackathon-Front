type InputDataDay = {
    day: number;
    hours: number[];
};

type InputDataMonth = {
    month: string;
    days: number[];
};
  
type OutputData = {
    labels: string[];
    datasets: {
      label: string;
      data: number[];
      backgroundColor: string[];
      // borderColor: string;
      tension: number;
      fill: boolean;
    }[];
};


export function convertDataDay(input: InputDataDay): OutputData {
    const { day, hours } = input;
    const labels = Array.from({ length: 24 }, (_, i) => (i + 1).toString() + ":00");
  
    const dataset = {
      label: `April ${day}`,
      data: hours,
      backgroundColor: ["rgba(38,132,241, 0.4)"],
      // borderColor: "rgba(75,192,192,1)",
      tension: 0.3,
      fill: true,
    };
  
    return {
      labels,
      datasets: [dataset],
    };
};


export function convertDataMonth(input: InputDataMonth): OutputData {
    const { month, days } = input;
    const labels = Array.from({ length: 30 }, (_, i) => (i + 1).toString());
  
    const dataset = {
      label: month,
      data: days,
      backgroundColor: ["rgba(18,102,241, 0.6)"],
      // borderColor: "rgba(75,192,192,1)",
      tension: 0.3,
      fill: true,
    };
  
    return {
      labels,
      datasets: [dataset],
    };
};

export const ManatKiloWattMin = 0.07;
export const ManatKiloWattMax = 0.11;


export function getTotal(dataset:number[]):number{
  const total = dataset.reduce((accumulator, currentValue) => accumulator + currentValue);
  return total;
}

export const getPrice = (total:number) => total>300? total*ManatKiloWattMax: total*ManatKiloWattMin;